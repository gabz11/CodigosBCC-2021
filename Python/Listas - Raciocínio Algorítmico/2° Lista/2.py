# os operadores lógicos são operadores que verificam varias condições
# AND faz uma condição depender da outra, é verdadeira quando a outra parte tbm é
# OR satisfaz caso uma ou outra forem verdade, caso uma ou outra for, é verdade
# NAND é verdadeiro quando pelo menos uma das variavés é falsa, se todas forem verdadeiras é falsa
# XOR é verdadeira quando as variaveis sejam diferentes, uma verdadeira e uma mentira,
# NOT negação, se for verdadeiro é falso, se for falso é verdadeiro